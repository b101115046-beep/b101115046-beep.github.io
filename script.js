const languageButtons = document.querySelectorAll('[data-lang]');
const elements = document.querySelectorAll('[data-i18n]');
const themeToggle = document.getElementById('themeToggle');

function applyLanguage(lang) {
  document.documentElement.lang = lang === 'en' ? 'en' : 'zh-Hant';
  elements.forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });
  languageButtons.forEach((button) => button.classList.toggle('active', button.dataset.lang === lang));
  localStorage.setItem('lang', lang);
}

function applyTheme(theme) {
  document.body.dataset.theme = theme;
  localStorage.setItem('theme', theme);
}

function animateCounters() {
  const counters = document.querySelectorAll('[data-count]');
  counters.forEach((counter) => {
    const target = Number(counter.dataset.count || 0);
    const duration = 1400;
    const startTime = performance.now();
    function update(now) {
      const progress = Math.min((now - startTime) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      counter.textContent = String(Math.round(target * eased));
      if (progress < 1) requestAnimationFrame(update);
      else counter.textContent = String(target);
    }
    requestAnimationFrame(update);
  });
}

function initAnimations() {
  if (window.gsap && window.ScrollTrigger) {
    gsap.registerPlugin(ScrollTrigger);
    const tl = gsap.timeline({ defaults: { ease: 'power3.out' } });
    tl.from('.hero-badge', { opacity: 0, y: 24, duration: 0.7 })
      .from('.hero-title', { opacity: 0, y: 30, duration: 0.8 }, '-=0.2')
      .from('.hero-subtitle', { opacity: 0, y: 24, duration: 0.7 }, '-=0.2')
      .from('.hero-copy-text', { opacity: 0, y: 24, duration: 0.7 }, '-=0.2')
      .from('.hero-actions, .hero-socials', { opacity: 0, y: 24, duration: 0.7, stagger: 0.08 }, '-=0.2')
      .from('.hero-visual', { opacity: 0, x: 50, duration: 0.95 }, '-=0.3');

    gsap.to('.hero-ring', { rotate: 360, duration: 22, ease: 'none', repeat: -1 });
    gsap.to('.ticker-track', { xPercent: -25, duration: 18, ease: 'none', repeat: -1 });

    gsap.utils.toArray('.reveal').forEach((element) => {
      gsap.from(element, {
        opacity: 0, y: 35, duration: 0.8, ease: 'power3.out',
        scrollTrigger: { trigger: element, start: 'top 85%' }
      });
    });

    gsap.utils.toArray('.story-card, .stat-card, .about-card, .contact-card, .extra-card, .archive-card, .focus-card, .experience-card, .diary-card').forEach((card) => {
      card.addEventListener('mouseenter', () => gsap.to(card, { y: -6, scale: 1.01, duration: 0.2, ease: 'power2.out' }));
      card.addEventListener('mouseleave', () => gsap.to(card, { y: 0, scale: 1, duration: 0.25, ease: 'power2.out' }));
    });

    document.querySelectorAll('.tilt-card').forEach((card) => {
      card.addEventListener('mousemove', (event) => {
        const rect = card.getBoundingClientRect();
        const rotateY = ((event.clientX - rect.left) / rect.width - 0.5) * 10;
        const rotateX = ((event.clientY - rect.top) / rect.height - 0.5) * -10;
        gsap.to(card, { rotateX, rotateY, transformPerspective: 900, duration: 0.25, ease: 'power2.out' });
      });
      card.addEventListener('mouseleave', () => gsap.to(card, { rotateX: 0, rotateY: 0, duration: 0.35, ease: 'power2.out' }));
    });

    const heroStage = document.querySelector('.hero-stage');
    const heroPortrait = document.querySelector('.hero-portrait');
    if (heroStage && heroPortrait) {
      heroStage.addEventListener('mousemove', (event) => {
        const rect = heroStage.getBoundingClientRect();
        const x = (event.clientX - rect.left) / rect.width - 0.5;
        const y = (event.clientY - rect.top) / rect.height - 0.5;
        gsap.to(heroStage, { rotateY: x * 8, rotateX: y * -8, duration: 0.25, ease: 'power2.out' });
        gsap.to(heroPortrait, { x: x * 12, y: y * 12, duration: 0.25, ease: 'power2.out' });
      });
      heroStage.addEventListener('mouseleave', () => {
        gsap.to(heroStage, { rotateY: 0, rotateX: 0, duration: 0.35, ease: 'power2.out' });
        gsap.to(heroPortrait, { x: 0, y: 0, duration: 0.35, ease: 'power2.out' });
      });
    }
  }
}

languageButtons.forEach((button) => button.addEventListener('click', () => applyLanguage(button.dataset.lang)));
themeToggle.addEventListener('click', () => applyTheme(document.body.dataset.theme === 'dark' ? 'light' : 'dark'));

document.addEventListener('DOMContentLoaded', () => {
  const storedLang = localStorage.getItem('lang') || 'zh-TW';
  const storedTheme = localStorage.getItem('theme') || 'dark';
  applyTheme(storedTheme);
  applyLanguage(storedLang);
  initAnimations();
  animateCounters();
});
